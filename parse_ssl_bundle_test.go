func TestExtractPEMCerts(t *testing.T) {

	testCases := []struct {
		desc             string
		bundle           []byte
		wantPEMCertCount int
		wantErrorCount   int
	}{
		{
			desc:             "Bundle with one cert block without leading or trailing characters",
			bundle:           []byte(fmt.Sprintf("%s", leafCertPEM)),
			wantPEMCertCount: 1,
			wantErrorCount:   0,
		},
		{
			desc:             "Bundle with one cert block with leading characters",
			bundle:           []byte(fmt.Sprintf("xxx%s", leafCertPEM)),
			wantPEMCertCount: 1,
			wantErrorCount:   0,
		},
		{
			desc:             "Bundle with one cert block with trailing characters after line break",
			bundle:           []byte(fmt.Sprintf("%s\nxxx", leafCertPEM)),
			wantPEMCertCount: 1,
			wantErrorCount:   0,
		},
		{
			desc:             "Bundle with two cert blocks without leading or trailing characters",
			bundle:           []byte(fmt.Sprintf("%s%s", leafCertPEM, leafCertPEM)),
			wantPEMCertCount: 2,
			wantErrorCount:   0,
		},
		{
			desc: `Bundle with two cert blocks with leading, trailing characters after
								line break, and other characters between blocks`,
			bundle:           []byte(fmt.Sprintf("xxx%s \nabc%s\nxxx", leafCertPEM, leafCertPEM)),
			wantPEMCertCount: 2,
			wantErrorCount:   0,
		},

		{
			desc: `Bundle with one cert block and one public key block with leading,
			 trailing characters after line break, and other characters between blocks`,
			bundle:           []byte(fmt.Sprintf("xxx%s \nabc%s\nxxx", leafCertPEM, pubPEM)),
			wantPEMCertCount: 1,
			wantErrorCount:   1,
		},
	}

	for _, tc := range testCases {

		t.Run(tc.desc, func(t *testing.T) {
			gotPEMCerts, gotBlockErrors := extractPEMCerts(tc.bundle)
			assert.Equal(t, tc.wantPEMCertCount, len(gotPEMCerts))
			assert.Equal(t, tc.wantErrorCount, len(gotBlockErrors))
		})
	}
}

func TestParsePEMCertificates(t *testing.T) {

	testCases := []struct {
		desc           string
		bundle         []byte
		wantCertCount  int
		wantErrorCount int
	}{
		{
			desc:           "Bundle with one valid PEM certificate",
			bundle:         []byte(fmt.Sprintf("%s", leafCertPEM)),
			wantCertCount:  1,
			wantErrorCount: 0,
		},
		{
			desc:           "Bundle with two valid PEM certificates",
			bundle:         []byte(fmt.Sprintf("%s%s", leafCertPEM, leafCertPEM)),
			wantCertCount:  2,
			wantErrorCount: 0,
		},
		{
			desc:           "Bundle with an invalid PEM certificates",
			bundle:         []byte(fmt.Sprintf("%s", invalidCertPEM)),
			wantCertCount:  0,
			wantErrorCount: 1,
		},
		{
			desc:           "Bundle with one valid and one invalid PEM certificate",
			bundle:         []byte(fmt.Sprintf("%s%s", leafCertPEM, invalidCertPEM)),
			wantCertCount:  1,
			wantErrorCount: 1,
		},
	}

	for _, tc := range testCases {

		t.Run(tc.desc, func(t *testing.T) {
			pemCerts, _ := extractPEMCerts(tc.bundle)
			gotCerts, gotCertErrors := parsePEMCertificates(pemCerts)
			assert.Equal(t, tc.wantCertCount, len(gotCerts.certs))
			assert.Equal(t, tc.wantErrorCount, len(gotCertErrors.errs))
		})
	}
}
